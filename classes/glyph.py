class Glyph:
    def __init__(self, level, hp, spirit, mag_atk, mag_def, phys_atk, phys_def, atk_level, def_level):
        self.level = level
        self.hp = hp
        self.spirit = spirit
        self.mag_atk = mag_atk
        self.mag_def = mag_def
        self.phys_atk = phys_atk
        self.phys_def = phys_def
        self.atk_level = atk_level
        self.def_level = def_level
