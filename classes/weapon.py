class Weapon:
	def __init__(self, name, type, mag_atk_min, mag_atk_max, phys_atk_min, phys_atk_max, attributes):
		self.name = name
		self.type = type
		self.mag_atk_min = mag_atk_min
		self.mag_atk_max = mag_atk_max
		self.phys_atk_min = phys_atk_min
		self.phys_atk_max = phys_atk_max
		self.attributes = attributes
